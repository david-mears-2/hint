<template>
    <modal :open="open">
        <h4 v-translate="'troubleshootingRequest'"></h4>
        <form class="form" id="report-form" v-if="!showFeedback">
            <div class="form-group" v-if="projectName">
                <label for="project" v-translate="'project'"></label>
                <input type="text"
                       disabled
                       id="project"
                       :value="projectName"
                       class="form-control"/>
            </div>
            <div class="form-group" v-if="isGuest">
                <label for="email" v-translate="'email'"></label>
                <input type="text"
                       id="email"
                       v-model="email"
                       class="form-control"/>
            </div>
            <div class="form-group">
                <label for="section" v-translate="'section'"></label>
                <select class="form-control"
                        v-model="currentSection"
                        id="section">
                    <option v-for="step in steps"
                            :key="step.number"
                            :value="step.textKey"
                            v-translate="step.textKey">
                    </option>
                    <option key="login"
                            v-translate="'login'"
                            value="login"></option>
                    <option key="projects" v-translate="'projects'"
                            value="projects"></option>
                    <option key="other"
                            value="other"
                            v-translate="'other'"></option>
                </select>
            </div>
            <div class="form-group">
                <label for="description"
                       class="mb-0"
                       v-translate="'description'"></label>
                <div class="small text-muted" v-translate="'errorDescriptionHelp'"></div>
                <textarea id="description"
                          v-model="description"
                          class="form-control"></textarea>
            </div>
            <div class="form-group">
                <label for="reproduce"
                       class="mb-0"
                       v-translate="'reproduce'"></label>
                <div class="small text-muted" v-translate="'reproduceErrorHelp'"></div>
                <textarea id="reproduce"
                          v-model="stepsToReproduce"
                          class="form-control"></textarea>
            </div>
        </form>
        <template v-else>
            <div id="report-success" v-if="!errorReportError" v-translate="'errorReportSuccess'"></div>
            <template v-else>
               <div id="report-error" v-translate="'errorReportError'"></div>
                <error-alert :error="errorReportError"></error-alert>
            </template>
        </template>
        <template v-slot:footer>
            <template v-if="!showFeedback">
                <div v-if="disabled" class="tooltip-wrapper"
                     style="cursor: not-allowed;"
                     v-tooltip="tooltipText">
                    <!-- tooltips can't be rendered on disabled elements -->
                    <button disabled
                            type="button"
                            class="btn btn-red"
                            v-translate="'send'"></button>
                </div>
                <button v-else
                        type="button"
                        id="send"
                        class="btn"
                        :class="sendingErrorReport? 'btn-secondary':'btn-red'"
                        :disabled="sendingErrorReport"
                        @click="sendErrorReport"
                        v-translate="'send'">
                </button>
                <button type="button"
                        class="btn btn-white"
                        @click="cancelErrorReport"
                        v-translate="'cancel'">
                </button>
            </template>
            <template v-else>
                <button type="button"
                        class="btn btn-red"
                        @click="close"
                        v-translate="'close'">
                </button>
            </template>
        </template>
    </modal>
</template>
<script lang="ts">
    import Vue from "vue"
    import {mapActionByName, mapGetterByName, mapStateProp} from "../utils";
    import {StepDescription, StepperState} from "../store/stepper/stepper";
    import {ProjectsState} from "../store/projects/projects"
    import Modal from "./Modal.vue";
    import {ErrorReportManualDetails} from "../types";
    import ErrorAlert from "./ErrorAlert.vue";
    import {VTooltip} from 'v-tooltip';
    import i18next from "i18next";
    import {RootState} from "../root";
    import {Language} from "../store/translations/locales";
    import {Error} from "../generated";
    import { ErrorsState } from "../store/errors/errors";


    interface Methods {
        generateErrorReport: (payload: ErrorReportManualDetails) => void
        sendErrorReport: () => void
        cancelErrorReport: () => void
        resetData: () => void
        close: () => void
    }

    interface Computed {
        currentLanguage: Language,
        currentSectionKey: string
        currentSection: string
        isGuest: boolean
        steps: StepDescription[]
        projectName: string | undefined
        disabled: boolean,
        tooltipText: string,
        errorReportError: Error | null
        sendingErrorReport: boolean
    }

    interface Props {
        open: boolean
    }

    interface Data extends ErrorReportManualDetails {
        showFeedback: boolean
    }

    export default Vue.extend<Data, Methods, Computed, Props>({
        components: {
            ErrorAlert,
            Modal
        },
        directives: {tooltip: VTooltip},
        props: {
            open: Boolean
        },
        name: "ErrorReport",
        data: function () {
            return {
                description: "",
                stepsToReproduce: "",
                section: "",
                email: "",
                showFeedback: false
            }
        },
        computed: {
            currentLanguage: mapStateProp<RootState, Language>(null, (state: RootState) => state.language),
            currentSectionKey: mapStateProp<StepperState, string>("stepper", state => {
                return state.steps[state.activeStep - 1].textKey;
            }),
            currentSection: {
                get() {
                    return this.section || this.currentSectionKey
                },
                set(newVal: string) {
                    this.section = newVal
                }
            },
            errorReportError: mapStateProp<ErrorsState, Error | null>(
                "errors",
                (state: ErrorsState) => state.errorReportError),
            sendingErrorReport: mapStateProp<ErrorsState, boolean>(
                "errors",
                (state: ErrorsState) => state.sendingErrorReport),
            isGuest: mapGetterByName(null, "isGuest"),
            projectName: mapStateProp<ProjectsState, string | undefined>("projects", state => state.currentProject?.name),
            steps: mapStateProp<StepperState, StepDescription[]>("stepper", state => state.steps),
            disabled() {
                return !this.description || !this.stepsToReproduce || (this.isGuest && !this.email)
            },
            tooltipText() {
                return i18next.t("allFieldsRequired", {lng: this.currentLanguage});
            }
        },
        methods: {
            generateErrorReport: mapActionByName(null, "generateErrorReport"),
            cancelErrorReport() {
                this.resetData();
                this.$emit("close")
            },
            async sendErrorReport() {
                await this.generateErrorReport({
                    section: this.currentSection,
                    description: this.description,
                    stepsToReproduce: this.stepsToReproduce,
                    email: this.email
                })
                this.resetData();
                this.showFeedback = true;
            },
            resetData() {
                this.description = "";
                this.stepsToReproduce = "";
                this.section = "";
                this.email = "";
            },
            close() {
                this.$emit("close");
            }
        },
        watch: {
            open(newVal) {
                if (newVal === true) {
                    this.showFeedback = false;
                    if (this.$route.path.indexOf("projects") > -1) {
                        this.section = "projects"
                    }
                }
            }
        }
    })
</script>
